

const age = 15;
const isOldEnough = age >= 18;

// curly parenthesis with condition and then block with {} gets executed
if (isOldEnough) {
    console.log("You are old enough to drive");
} else {
    const yearsLeft = 18 - age;
    console.log(`You are not old enough to drive. Wait another ${yearsLeft} years to drive.`);
}

